    private void botaoAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAtualizarActionPerformed
        String auxPerecivel;
 
        if (radioMasc.isSelected()) {
            auxPerecivel = " Masculino ";

        } else {
            auxPerecivel = "Feminino";
        }

        Funcionario funcionario = new Funcionario(auxcodigo,campoFuncionario.getText(), Double.parseDouble(campoSalario.getText()), campoCpf.getText(), campoEndereco.getText(),
                campoTelefone.getText(), Integer.parseInt(campoMatricula.getText()), CFuncionario.converteStringDate(campoData.getText()), auxPerecivel);
        if (new CFuncionario().atualizarFuncionario(funcionario)) {
            JOptionPane.showMessageDialog(null, "Registro atualizado com sucesso!");
        } else {
            JOptionPane.showMessageDialog(null, "Falha ao atualizar registro! Tente novamente.");
        }
        this.dispose();
        new FormularioAlterarFuncionarios().setVisible(true);
    }//GEN-LAST:event_botaoAtualizarActionPerformed

