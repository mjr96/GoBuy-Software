    public FormularioAlterarFuncionarios(Funcionario funcionario) {
        initComponents();
        String auxSexo;

        if (radioMasc.isSelected()) {
            auxSexo = "Masculino";

        } else {
            auxSexo = "Feminino";
        }

        if (funcionario.getSexo().equals("Masculino")) {
            radioMasc.setSelected(true);
        } else {
            radioFem.setSelected(true);
        }

        String[] auxDataAdmicaoVetor = CFuncionario.converteDataString(funcionario.getDataAdmicao())
                .split("/");
        String auxDataAdmicao = "";
        for (int i = 0; i < auxDataAdmicaoVetor.length; i++) {
            auxDataAdmicao += auxDataAdmicaoVetor[i];
        }
        auxcodigo = funcionario.getId();
        campoFuncionario.setText(funcionario.getNomeFuncionario());
        campoSalario.setText(Double.toString(funcionario.getSalarioFuncionario()));
        campoCpf.setText(funcionario.getCpf());
        campoEndereco.setText(funcionario.getEndereco());
        campoTelefone.setText(funcionario.getTelefone());
        campoMatricula.setText(Integer.toString(funcionario.getMatricula()));
        campoData.setText(auxDataAdmicao);

    }

